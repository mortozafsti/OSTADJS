
// simple function

function simpleFunction(){
    var x = 10;
    var y = 20;
    var z = x+y;

    console.log("It's simple function"+z);
}
simpleFunction();

// parameterized function

function parameterizedFunction(x,y){
    var z = x + y;
    console.log("It's parameterized function  "+z);
}

parameterizedFunction(900,20);